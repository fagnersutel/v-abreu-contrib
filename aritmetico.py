def adicao(a, b):
# Esta função calcula a soma dos números a e b
    return a + b

def subtracao(a, b):
# Esta função calcula a subtracao dos números a e b
    return a - b

def multiplicacao(a, b):
# Esta função calcula a multiplicação dos números a e b
    return a * b

def divisao(a, b):
# Esta função calcula a divisão dos números a e b
    return a / b